# ── Fallback chain: any missing lang falls back to English ──
DEFAULT_LANG = "it"
FALLBACK_LANG = "en"

# ── Global state ──
_current_lang = DEFAULT_LANG


def detect_system_language() -> str:
    """Detect the system language and return a supported language code."""
    try:
        # Windows: returns something like ('it_IT', 'cp1252') or ('en_US', 'UTF-8')
        sys_locale = locale.getdefaultlocale()[0]
        if sys_locale:
            lang_code = sys_locale[:2].lower()
            if lang_code in TRANSLATIONS:
                logger.info(f"Detected system language: {lang_code}")
                return lang_code
            logger.info(f"System language '{lang_code}' not supported, falling back to '{DEFAULT_LANG}'")
    except Exception as e:
        logger.warning(f"Could not detect system language: {e}")
    return DEFAULT_LANG


def set_language(lang_code: str):
    """Set the current language."""
    global _current_lang
    if lang_code in TRANSLATIONS:
        _current_lang = lang_code
        logger.info(f"Language set to: {lang_code} ({LANGUAGES.get(lang_code, '?')})")
    else:
        logger.warning(f"Language '{lang_code}' not supported, keeping '{_current_lang}'")


def get_language() -> str:
    """Get the current language code."""
    return _current_lang


def t(key: str, **kwargs) -> str:
    """
    Get a translated string for the given key.
    Supports {placeholder} substitution via kwargs.
    Falls back to English if key is missing in current language.
    """
    lang_dict = TRANSLATIONS.get(_current_lang, TRANSLATIONS[FALLBACK_LANG])
    text = lang_dict.get(key)

    if text is None:
        # Try fallback
        text = TRANSLATIONS[FALLBACK_LANG].get(key, f"[{key}]")

    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass  # Return raw string if formatting fails

    return text
